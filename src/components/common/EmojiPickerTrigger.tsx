interface EmojiPickerTriggerProps {
  onEmojiSelected: (emoji: string) => void;
  currentEmoji?: string;
  label?: string;
  buttonStyle?: React.CSSProperties;
  className?: string;
}

/**
 * A component that allows selecting an emoji.
 */
const EmojiPickerTrigger: React.FC<EmojiPickerTriggerProps> = ({ 
  onEmojiSelected, 
  currentEmoji = '❓', 
  label = 'Select Emoji',
  buttonStyle = {},
  className
}) => {
  const [showEmojiGrid, setShowEmojiGrid] = useState(false);
  const [currentCategory, setCurrentCategory] = useState(0);
  const containerRef = useRef<HTMLDivElement>(null);
  
  const toggleEmojiGrid = () => {
    setShowEmojiGrid(!showEmojiGrid);
  };

  const handleEmojiSelect = (emoji: string) => {
    onEmojiSelected(emoji);
    setShowEmojiGrid(false);
  };
  
  // Handle clicking outside of the emoji picker
  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (containerRef.current && !containerRef.current.contains(event.target as Node)) {
        setShowEmojiGrid(false);
      }
    };

    if (showEmojiGrid) {
      document.addEventListener('mousedown', handleClickOutside);
    }
    
    return () => {
      document.removeEventListener('mousedown', handleClickOutside);
    };
  }, [showEmojiGrid]);
  
  return (
    <div className={className} style={{ position: 'relative' }} ref={containerRef}>
      <EmojiButton
        onClick={toggleEmojiGrid}
        whileHover={{ scale: 1.05 }}
        whileTap={{ scale: 0.95 }}
        style={buttonStyle}
      >
        <span>{currentEmoji}</span>
        {label && <span>{label}</span>}
      </EmojiButton>
      
      {showEmojiGrid && (
        <EmojiGridContainer
          initial={{ opacity: 0, x: 10 }}
          animate={{ opacity: 1, x: 0 }}
          exit={{ opacity: 0, x: 10 }}
          onClick={(e) => e.stopPropagation()}
          drag
          dragConstraints={{ left: -300, right: 300, top: -300, bottom: 300 }}
          dragElastic={0.1}
          dragMomentum={false}
        >
          <div className="drag-handle">Drag to move</div>
          <CategoryRow>
            {emojiCategories.map((category, index) => (
              <CategoryButton 
                key={category.name}
                active={currentCategory === index}
                onClick={() => setCurrentCategory(index)}
              >
                {category.name}
              </CategoryButton>
            ))}
          </CategoryRow>
          
          <EmojiGrid>
            {emojiCategories[currentCategory].emojis.map(emoji => (
              <EmojiItem 
                key={emoji} 
                onClick={() => handleEmojiSelect(emoji)}
              >
                {emoji}
              </EmojiItem>
            ))}
          </EmojiGrid>
        </EmojiGridContainer>
      )}
    </div>
  );
};

export default EmojiPickerTrigger; 